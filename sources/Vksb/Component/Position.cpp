///////////////////////////////////////////////////////////////////////////
///
///////////////////////////////////////////////////////////////////////////
void ::vksb::component::Position::update(
    float deltaTime,
    ::vksb::component::Control& control,
    const ::glm::vec3& direction
)
{
    // search the number of directions moving in and removing speed when multiple direction at once
    auto speedDirectionDivider{ 1.0f };

    // bot top
    if (control.isAbleToFly()) {
        if (control.isMovingUp()) {
            if (!control.isMovingDown()) {
                speedDirectionDivider /= 2;
            }
            speedDirectionDivider /= 2;
        } else if (control.isMovingDown()) {
            speedDirectionDivider /= 2;
        }
    }

    // left right
    if (control.isMovingLeft()) {
        if (!control.isMovingRight()) {
            speedDirectionDivider /= 2;
        }
    } else if (control.isMovingRight()) {
        speedDirectionDivider /= 2;
    }

    // forward backward
    if (control.isMovingForward()) {
        if (!control.isMovingBackward()) {
            speedDirectionDivider /= 2;
        }
    } else if (control.isMovingBackward()) {
        speedDirectionDivider /= 2;
    } else if (speedDirectionDivider == 2) {
        return; // not any direction
    }



    // apply movement
    auto velocity{ control.getSpeed() * deltaTime * speedDirectionDivider / 10000 };

    // bot top
    if (control.isAbleToFly()) {
        if (control.isMovingUp()) {
            if (!control.isMovingDown()) {
                this->moveUp(velocity, direction);
            }
            speedDirectionDivider /= 2;
        } else if (control.isMovingDown()) {
            this->moveDown(velocity, direction);
        }
    }

    // left right
    if (control.isMovingLeft()) {
        if (!control.isMovingRight()) {
            this->moveLeft(velocity, direction);
        }
    } else if (control.isMovingRight()) {
        this->moveRight(velocity, direction);
    }

    // forward backward
    if (control.isMovingForward()) {
        if (!control.isMovingBackward()) {
            this->moveForward(velocity, direction);
        }
    } else if (control.isMovingBackward()) {
        this->moveBackward(velocity, direction);
    }
}



///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
// Position
//
///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////

///////////////////////////////////////////////////////////////////////////
void ::vksb::component::Position::moveForward(
    const float velocity,
    const ::glm::vec3& direction
)
{
    m_position += velocity * direction;
    m_isChanged = true;
}

///////////////////////////////////////////////////////////////////////////
void ::vksb::component::Position::moveBackward(
    const float velocity,
    const ::glm::vec3& direction
)
{
    m_position -= velocity * direction;
    m_isChanged = true;
}

///////////////////////////////////////////////////////////////////////////
void ::vksb::component::Position::moveRight(
    const float velocity,
    const ::glm::vec3& direction
)
{
    m_position += ::glm::normalize(::glm::cross(direction, ::glm::vec3{ 0.0f, 1.0f, 0.0f })) * velocity;
    m_isChanged = true;
}

///////////////////////////////////////////////////////////////////////////
void ::vksb::component::Position::moveLeft(
    const float velocity,
    const ::glm::vec3& direction
)
{
    m_position -= ::glm::normalize(::glm::cross(direction, ::glm::vec3{ 0.0f, 1.0f, 0.0f })) * velocity;
    m_isChanged = true;
}

///////////////////////////////////////////////////////////////////////////
void ::vksb::component::Position::moveUp(
    const float velocity,
    const ::glm::vec3& direction
)
{
    m_position.y -= velocity;
    m_isChanged = true;
}

///////////////////////////////////////////////////////////////////////////
void ::vksb::component::Position::moveDown(
    const float velocity,
    const ::glm::vec3& direction
)
{
    m_position.y += velocity;
    m_isChanged = true;
}

///////////////////////////////////////////////////////////////////////////
void ::vksb::component::Position::move(
    const ::glm::vec3& offset
)
{
    m_position += offset;
    m_isChanged = true;
}

///////////////////////////////////////////////////////////////////////////
void ::vksb::component::Position::move(
    const float offsetX,
    const float offsetY,
    const float offsetZ
)
{
    m_position += ::glm::vec3{ offsetX, offsetY, offsetZ };
    m_isChanged = true;
}

///////////////////////////////////////////////////////////////////////////
void ::vksb::component::Position::moveX(
    const float offset
)
{
    m_position.x += offset;
    m_isChanged = true;
}

///////////////////////////////////////////////////////////////////////////
void ::vksb::component::Position::moveY(
    const float offset
)
{
    m_position.y -= offset;
    m_isChanged = true;
}

///////////////////////////////////////////////////////////////////////////
void ::vksb::component::Position::moveZ(
    const float offset
)
{
    m_position.z += offset;
    m_isChanged = true;
}



