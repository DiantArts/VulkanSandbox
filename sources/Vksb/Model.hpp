#pragma once

#include <Vksb/Device.hpp>
#include <Vksb/Buffer.hpp>
#include <Vksb/Hash.hpp>

namespace vksb {

///////////////////////////////////////////////////////////////////////////
/// \brief No clue what it does xD
/// \ingroup vulkan
///
/// \include Model.hpp <Model.hpp>
///
///////////////////////////////////////////////////////////////////////////
class Model {

public:

    ///////////////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////
    // Static
    //
    ///////////////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////
    ///
    ///////////////////////////////////////////////////////////////////////////
    struct Vertex {
        ::glm::vec3 position{};
        ::glm::vec3 color{};
        ::glm::vec3 normal{};
        ::glm::vec2 uv{};

        [[ nodiscard ]] static auto getBindingDescriptions()
            -> ::std::vector<::VkVertexInputBindingDescription>;

        [[ nodiscard ]] static auto getAttributeDescriptions()
            -> ::std::vector<::VkVertexInputAttributeDescription>;

        [[ nodiscard ]] auto operator==(
            const Vertex &other
        ) const
            -> bool;
    };

    ///////////////////////////////////////////////////////////////////////////
    ///
    ///////////////////////////////////////////////////////////////////////////
    struct Builder {
        ::std::vector<Vertex> vertices;
        ::std::vector<::std::uint32_t> indices;

        void loadFromFile(
            ::std::string_view filename
        );
    };

    ///////////////////////////////////////////////////////////////////////////
    ///
    ///////////////////////////////////////////////////////////////////////////
    [[ nodiscard ]] static auto createFromFile(
        ::vksb::Device& device,
        ::std::string_view filename
    ) -> ::std::unique_ptr<::vksb::Model>;



public:

    ///////////////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////
    // Constructors
    //
    ///////////////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////
    /// \brief Default constructor
    ///
    ///////////////////////////////////////////////////////////////////////////
    explicit Model(
        ::vksb::Device& m_device,
        const Model::Builder& builder
    );



    ///////////////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////
    // Rule of 5
    //
    ///////////////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////
    /// \brief Destructor
    ///
    ///////////////////////////////////////////////////////////////////////////
    ~Model();

    ///////////////////////////////////////////////////////////////////////////
    /// \brief Copy constructor
    ///
    ///////////////////////////////////////////////////////////////////////////
    Model(
        const Model&
    ) noexcept = delete;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief Copy assign operator
    ///
    ///////////////////////////////////////////////////////////////////////////
    auto operator=(
        const Model&
    ) noexcept
        -> Model& = delete;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief Move constructor
    ///
    ///////////////////////////////////////////////////////////////////////////
    Model(
        Model&&
    ) noexcept = delete;

    ///////////////////////////////////////////////////////////////////////////
    /// \brief Move assign operator
    ///
    ///////////////////////////////////////////////////////////////////////////
    auto operator=(
        Model&&
    ) noexcept
        -> Model& = delete;



    ///////////////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////
    // Basic
    //
    ///////////////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////
    ///
    ///////////////////////////////////////////////////////////////////////////
    void bind(
        ::VkCommandBuffer commandBuffer
    );

    ///////////////////////////////////////////////////////////////////////////
    ///
    ///////////////////////////////////////////////////////////////////////////
    void draw(
        ::VkCommandBuffer commandBuffer
    );



private:

    ///////////////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////
    // Helpers
    //
    ///////////////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////
    ///
    ///////////////////////////////////////////////////////////////////////////
    void createVertexBuffers(
        const ::std::vector<Model::Vertex>& vertices
    );

    ///////////////////////////////////////////////////////////////////////////
    ///
    ///////////////////////////////////////////////////////////////////////////
    void createIndexBuffers(
        const ::std::vector<::std::uint32_t>& vertices
    );



private:

    ///////////////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////
    // Member variables
    //
    ///////////////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////

    ::vksb::Device& m_device;

    // vertex
    ::std::unique_ptr<::vksb::Buffer> m_vertexBuffer;
    ::std::uint32_t m_vertexCount;

    // index
    bool m_hasIndexBuffer{ false };
    ::std::unique_ptr<::vksb::Buffer> m_indexBuffer;
    ::std::uint32_t m_indexCount;

};

} // namespace vksb
